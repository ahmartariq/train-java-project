    private void btnproceedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnproceedMouseClicked
        if(txtfname.getText().trim().isEmpty() || txtfcnic.getText().trim().isEmpty() || txtfnumber.getText().trim().isEmpty() ||
          txtfemail.getText().trim().isEmpty()){
               alertmessage.setText("Error: Fill all fields.");
        }else{
            name = txtfname.getText();
            cnic = txtfcnic.getText();
            number = txtfnumber.getText();
            email = txtfemail.getText();
            seat = (int) spnseats.getValue();
            if(seat==0){
                alertmessage.setText("Please select number of seats");
            }else if(txtfcnic.getText().length()!=13){
                alertmessage.setText("Please Enter 13-digit CNIC.");
            }else if (txtfnumber.getText().length()!=11){
                alertmessage.setText("Please Enter 11-digit Phone Number.");
            }else{
                Class = cmbclass.getItemAt(cmbclass.getSelectedIndex());
            switch (Class) {
                case "Select class":
                    alertmessage.setText("Error: Select Class");
                    break;
                case "Business":
                    if(s5>=seat){ //s5 are available seats adn seat is Seats selected by user.
                        eseats = s4;
                        bseats = s5-seat;
                        acseats = s6;
                        total= s8*seat; //s8 is fare of economy class.
                        String t = Integer.toString(total);
                        String s = Integer.toString(s8);
                        txtffoneticket.setText(s);
                        txtfftotalfair.setText(t);
                        alertmessage.setText("");
                        btnproceed.setVisible(false);
                        btnback.setVisible(false);
                        pnlsure.setVisible(true);
                    }else{
                        alertmessage.setText("Business class only have "+s5+" seats");
                    }   break;
                case "Economy":
                    if(s4>=seat){
                        eseats = s4-seat;
                        bseats =s5;
                        acseats =s6;
                        total= s7*seat;
                        String t = Integer.toString(total);
                        String s = Integer.toString(s7);
                        txtffoneticket.setText(s);
                        txtfftotalfair.setText(t);
                        alertmessage.setText("");
                        btnproceed.setVisible(false);
                        btnback.setVisible(false);
                        pnlsure.setVisible(true);
                    }else{
                        alertmessage.setText("Economy class only have "+s4+" seats");
                    }   break;
                case "AC Parlour":
                    if(s6>=seat){
                        eseats = s4;
                        bseats = s5;
                        acseats = s6-seat;
                        total= s9*seat;
                        String t = Integer.toString(total);
                        String s = Integer.toString(s9);
                        txtffoneticket.setText(s);
                        txtfftotalfair.setText(t);
                        alertmessage.setText("");
                        btnproceed.setVisible(false);
                        btnback.setVisible(false);
                        pnlsure.setVisible(true);
                    }else{ 
                        alertmessage.setText("AC Parlour class only have "+s6+" seats");
                    }   break;
                default:
                    break;
            }
          } 
        }
    }//GEN-LAST:event_btnproceedMouseClicked

